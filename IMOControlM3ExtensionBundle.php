namespace IMOControl\M3\ExtensionBundle;

use Symfony\Component\HttpKernel\Bundle\Bundle;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use IMOControl\M3\ExtensionBundle\DependencyInjection\Compiler\AddMapperInformationCompilerPass;

class IMOControlM3ExtensionBundle extends Bundle
{
    /**
     * Builds the bundle.
     *
     * It is only ever called once when the cache is empty.
     *
     * This method can be overridden to register compilation passes,
     * other extensions, ...
     *
     * @param ContainerBuilder $container A ContainerBuilder instance
     */
    public function build(ContainerBuilder $container)
    {
        $container->addCompilerPass(new AddMapperInformationCompilerPass);
    }
}


